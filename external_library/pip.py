# --- pip : 파이썬 모듈 및 패키지를 쉽게 설치할 수 있도록 도와주는 도구

## pip install
### $ pip install {package_name}

## pip uninstall
### $ pip uninstall {package_name}

## 특정 버전으로 설치하기
### $ pip install {package_name}==1.0.4 # 생략하면 최신 버전 설치

## 최신 버전으로 업그레이드하기
### $ pip install --upgrade {package_name}

## 설치된 패키지 확인하기
### $ pip list

